#Create a set...
set = set()

#Adding elements...
set.add(10)
set.add(11)
set.add(12)
set.add(13)
set.add(14)

#Printing the set...
print("set :", set)

#Modify... 
set.remove(12)
set.add(15)

#Printing the set after modification...
print("Set after modification:", set)

#Removing an element...
set.remove(11)

#Printing the set after remove...
print("Set after removal:", set)